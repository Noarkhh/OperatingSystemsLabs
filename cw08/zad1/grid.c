#include "grid.h"


const int grid_width = GRIDWIDTH;
const int grid_height = GRIDHEIGHT;

char *create_grid()
{
    return malloc(sizeof(char) * grid_width * grid_height);
}

void destroy_grid(char *grid)
{
    free(grid);
}

void draw_grid(char *grid)
{
    for (int i = 0; i < grid_height; ++i)
    {
        // Two characters for more uniform spaces (vertical vs horizontal)
        for (int j = 0; j < grid_width; ++j)
        {
            if (grid[i * grid_width + j])
            {
                mvprintw(i, j * 2, "â– ");
                mvprintw(i, j * 2 + 1, " ");
            }
            else
            {
                mvprintw(i, j * 2, " ");
                mvprintw(i, j * 2 + 1, " ");
            }
        }
    }

    refresh();
}

void init_grid(char *grid)
{
    for (int i = 0; i < grid_width * grid_height; ++i)
        grid[i] = rand() % 2 == 0;
}

bool is_alive(int row, int col, char *grid)
{

    int count = 0;
    for (int i = -1; i <= 1; i++)
    {
        for (int j = -1; j <= 1; j++)
        {
            if (i == 0 && j == 0)
            {
                continue;
            }
            int r = row + i;
            int c = col + j;
            if (r < 0 || r >= grid_height || c < 0 || c >= grid_width)
            {
                continue;
            }
            if (grid[grid_width * r + c])
            {
                count++;
            }
        }
    }

    if (grid[row * grid_width + col])
    {
        if (count == 2 || count == 3)
            return true;
        else
            return false;
    }
    else
    {
        if (count == 3)
            return true;
        else
            return false;
    }
}

void* update_cell(void* args_void) {
    UpdateCellArgs* args = (UpdateCellArgs*) args_void;
    while (1) {
        pause();
        args->dst[args->i * grid_width + args->j] = is_alive(args->i, args->j, args->src);
        char* tmp = args->src;
        args->src = args->dst;
        args->dst = tmp;
    }
    return NULL;
}

void update_grid(char *src, char *dst, pthread_t* threads) {
    for (int i = 0; i < GRIDHEIGHT * GRIDWIDTH; i++) {
        pthread_kill(threads[i], SIGUSR1);
    }
}

